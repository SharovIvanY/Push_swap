#include "libft.h"

void	ft_print_params(int a, char **str)
{
	int i;
	int j;

	j = 1;
	i = 1;
	while (i < a)
	{
		ft_putstr(str[j]);
		ft_putchar('\n');
		j++;
		i++;
	}
}
