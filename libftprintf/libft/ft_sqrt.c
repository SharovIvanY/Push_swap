#include "libft.h"

int		ft_sqrt(int nb)
{
	int a;
	int i;
	int j;

	a = 1;
	i = 1;
	j = 1;
	if (nb <= 0)
		return (0);
	while (j < nb)
	{
		a = a + 2;
		j = j + a;
		i++;
	}
	if (j == nb)
		return (i);
	return (0);
}
